/* An example to show the use of two dimensiuonal arrays. */

class Matrix 
{
	public static void main(String[] args) 
	{
		int a[][] = {
			           {11,13,15},
			           {12,14,16},
			           {19,18,17}
					  };
     
	 for(int i=0;i<3;i++)
		{
          for(int j=0;j<3;j++)
			{
				System.out.print("  "+a[i][j]);
			}
		  System.out.println();		
		}
	}
}

class MathTest 
{
	public static void main(String[] args) 
	{

		System.out.println("E Value : "+Math.E);
		System.out.println("PI Value : "+Math.PI);
		System.out.println("2 power 3 is :"+Math.pow(2,3));
		System.out.println("Max Value (10,15) : "+Math.max(10,15));
		System.out.println("Min Value (10,15) : "+Math.min(10,15));
		
		System.out.println("Round Value(2.34) : "+Math.round(2.34));
		System.out.println("Round Value(2.54) : "+Math.round(2.54));

		System.out.println("Ceil Value(2.34) : "+Math.ceil(2.34));
		System.out.println("Ceil Value(2.54) : "+Math.ceil(2.54));

		System.out.println("Floor Value(2.34) : "+Math.floor(2.34));
		System.out.println("Floor Value(2.54) : "+Math.floor(2.54));
		System.out.println("Square Root (16) : "+Math.sqrt(16));
		
   }
}
